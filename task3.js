class calculator {
    static add = (a, b) => {
      return a + b;
    }
    static substract = (a, b) => {
      return a - b;
    }
    static multiply = (a, b) => {
      return a * b;
    }
    static devide = (a, b) => {
      return a / b;
    }
  }
  
  module.exports = calculator;